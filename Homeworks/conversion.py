'''
Pseudo code:

set variable 'num1'
set variable 'num2'
set variable 'num3'
set variable 'string1'

convert 'num1' into integer
convert 'num2' into a float
convert 'num3' into a String
convert 'string1' into an integer

output 'num1'
output 'num2'
output 'num3'
output 'string1'
'''

# Starting variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# num1 into integer
num1 = int(num1)

# num2 into a float
num2 = float(num2)

# num3 into a String
num3 = str(num3)

# string1 into an integer
string1 = int(string1)

# Prints everything on separate lines
print(num1)
print(num2)
print(num3)
print(string1)